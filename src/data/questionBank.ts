/**
 * Build an ordered question set for a session: draw from the requested
 * categories in priority order, round-robin, up to `count`. If those pools
 * run dry, top up from related categories so a focused mode still fills a
 * long interview rather than ending after a handful of questions.
 */
export function buildQuestionSet(categories: Category[], count: number): BankQuestion[] {
  const used = new Set<string>();
  const out: BankQuestion[] = [];

  const drain = (cats: Category[]) => {
    const pools = cats.map((c) => shuffle(questionsIn(c).filter((q) => !used.has(q.id))));
    let idx = 0;
    let guard = 0;
    while (out.length < count && pools.some((p) => p.length > 0) && guard++ < 500) {
      const pool = pools[idx % pools.length];
      const q = pool.shift();
      if (q && !used.has(q.id)) {
        used.add(q.id);
        out.push(q);
      }
      idx++;
    }
  };

  drain(categories);

  if (out.length < count) {
    const fallback = [...new Set(categories.flatMap((c) => RELATED[c] ?? []))].filter(
      (c) => !categories.includes(c)
    );
    if (fallback.length) drain(fallback);
  }

  return out;
}

function shuffle<T>(arr: T[]): T[] {
  const a = [...arr];
  for (let i = a.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [a[i], a[j]] = [a[j], a[i]];
  }
  return a;
}
